public class Constants {

    public static void main(String[] args) {
        
        // 상수(constants, final variables)
        // 한 번 값이 할당 => 재할당이 불가능
        // 전체를 대문자, 단어간 구분은 _

        final double PI = 3.14;
        System.out.println(PI);

        //PI = 3.1415926535; -> final 로 할당한건 재할당 불가능


    }
}
